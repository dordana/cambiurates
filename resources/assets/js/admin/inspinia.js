function SmoothlyMenu() {
    if (!$('body').hasClass('mini-navbar') || $('body').hasClass('body-small')) {
        // Hide menu in order to smoothly turn on when maximize menu
        $('#side-menu').hide();
        // For smoothly turn on menu
        setTimeout(
            function () {
                $('#side-menu').fadeIn(400);
            }, 200);
    } else if ($('body').hasClass('fixed-sidebar')) {
        $('#side-menu').hide();
        setTimeout(
            function () {
                $('#side-menu').fadeIn(400);
            }, 100);
    } else {
        // Remove all inline style from jquery fadeIn function to reset menu state
        $('#side-menu').removeAttr('style');
    }
}

// Dragable panels
function WinMove(url, token) {
    var element = "[class*=items-draggable]";
    var handle = ".ibox-title";
    var connect = "[class*=col]";
    $(element).sortable(
        {
            axis: 'y',
            stop: function (event, ui) {
                var data = $(this).sortable('serialize');
                data += '&_token=' + token;
                // POST to server using $.post or $.ajax
                $.ajax({
                    data: data,
                    type: 'POST',
                    url: url
                });
            },
            handle: handle,
            connectWith: connect,
            tolerance: 'pointer',
            forcePlaceholderSize: true,
            opacity: 0.8
        })
    //.disableSelection();
}

function multiple_delete(url, csrf_token) {
    $('.delete').on('click', function(){
        var data = [];

        $.each($('.i-checks:checked').iCheck('data'), function( key, value ) {
            data.push($(value).val());
        });

        $.ajax({
            method: "POST",
            url: url,
            data: {
                item: data,
                _token: csrf_token
            },
            success: function(result) {
                var data = $.parseJSON(result);

                if (data.success) {
                    $.each(data.data, function( key, value ) {
                        $('.item_' + value).remove();
                    });
                }
            }
        })
    });
}

function multiple_is_read(url, csrf_token) {
    $('.is_read').on('click', function(){
        var data = [];

        $.each($('.i-checks:checked').iCheck('data'), function( key, value ) {
            data.push($(value).val());
        });

        $.ajax({
            method: "POST",
            url: url,
            data: {
                item: data,
                _token: csrf_token
            },
            success: function(result) {
                var data = $.parseJSON(result);

                if (data.success) {
                    $.each(data.data, function( key, value ) {
                        var data = $('.item_' + value);

                        data.removeClass('unread').addClass('read');
                        data.find('input[name="delete[]"]').iCheck('uncheck');
                    });
                }
            }
        })
    });
}

